package home.task23;

public class Node {

    private String data;
    private Node next;

    public Node() {
    }

    public void setData(String data) {
        this.data = data;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public String getData() {
        return data;
    }

    public Node getNext() {
        return next;
    }




}


